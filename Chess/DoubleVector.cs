using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

class DoubleVector
{
    public int x1;
    public int y1;
    public int x2;
    public int y2;

    public DoubleVector(int x1In, int y1In, int x2In, int y2In)
    {
        x1 = x1In;
        y2 = y1In;
        x1 = x2In;
        y2 = y2In;
    }
}